public class fraction
{
    public dynamic Sum(dynamic firstValue, dynamic secondValue)
        => firstValue + secondValue;

    public dynamic Mulit(dynamic firstValue, dynamic secondValue)
        => firstValue * secondValue;

    public dynamic Division(dynamic firstValue, dynamic secondValue)
        => firstValue / secondValue;

    public dynamic Submission(dynamic firstValue, dynamic secondValue)
        => firstValue - secondValue;
}